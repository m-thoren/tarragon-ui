export const generateLogLineWithGradient = (): string => {
	const terminalWidth = process.stdout.columns || 80
	const colors = Object.values(MsgColor).filter((value) => value !== MsgColor.Reset)
	const colorCount = colors.length

	let gradientLine = ''
	for (let i = 0; i < terminalWidth; i++) {
		const colorIndex = i % colorCount
		gradientLine += `${colors[colorIndex]}-`
	}
	gradientLine += MsgColor.Reset

	return gradientLine
}

export function randomAscii(): string {
	const randomIndex = Math.floor(Math.random() * ASCII_ARTS.length)
	return ASCII_ARTS[randomIndex]
}

